/// Template that generates a minimal mage host binary.
///
/// This is generation zero — the first binary bootstrapped from workspace
/// sources. It wires up the agent loop, registers discovered extensions,
/// and starts an interactive or single-prompt session.
struct BootstrapTemplate;

impl Template for BootstrapTemplate {
    fn render_main(&self, ctx: &RenderContext) -> Result<String> {
        let mut mod_decls = String::new();
        let mut init_calls = String::new();

        for m in ctx.modules {
            let mod_name = m.name.replace('-', "_");

            // Only include modules that have an init function.
            if m.init.is_some() {
                mod_decls.push_str(&format!(
                    "#[path = \"modules/{}.rs\"]\nmod {mod_name};\n",
                    m.name
                ));
                init_calls.push_str(&format!("    {mod_name}::init(&mut registry);\n"));
            }
        }

        Ok(format!(
            r#"//! Generated by `cargo xtask bootstrap` — do not edit.
use mage_core::extension::Registry;

{mod_decls}
fn main() {{
    let rt = tokio::runtime::Builder::new_current_thread()
        .enable_all()
        .build()
        .expect("failed to build tokio runtime");

    rt.block_on(async {{
        let mut tools = Vec::new();
        let mut providers = Vec::new();
        let mut registry = Registry {{
            tools: &mut tools,
            providers: &mut providers,
        }};
{init_calls}
        eprintln!("mage: bootstrap binary ready");
        eprintln!("mage: {{}} tool(s) registered", tools.len());
        eprintln!("mage: {{}} provider(s) registered", providers.len());
    }});
}}
"#
        ))
    }

    fn render_dependencies(&self, _ctx: &RenderContext) -> Result<Vec<Dependency>> {
        // The bootstrap binary needs mage-core and tokio at minimum.
        // These come from core_crates — no extra deps needed from the template.
        Ok(vec![])
    }
}

fn bootstrap() -> Result<()> {
    let root = workspace_root();
    eprintln!("workspace root: {}", root.display());

    // Scan extensions/ for any .rs modules.
    let ext_dir = root.join("extensions").join("tools-coding");
    let mut modules = Vec::new();

    if ext_dir.is_dir() {
        for entry in std::fs::read_dir(&ext_dir)? {
            let entry = entry?;
            let path = entry.path();
            if path.extension().is_some_and(|e| e == "rs") {
                let name = path.file_stem().unwrap_or_default().to_string_lossy();
                match Module::parse_file(&path, &name) {
                    Ok(m) => {
                        eprintln!("  found extension: {} ({})", m.name, path.display());
                        modules.push(m);
                    }
                    Err(e) => {
                        eprintln!("  warning: failed to parse {}: {e}", path.display());
                    }
                }
            }
        }
    }

    if modules.is_empty() {
        eprintln!("  no extensions found (extensions/tools-coding/ is empty)");
    }

    // Build the bundle with core crates from the workspace.
    let config = Config {
        approot: root.join("target/mage-bootstrap"),
        ..Config::default()
    };

    let mut bundle = Bundle::new("mage-bootstrap")
        .with_config(config)
        .add_core_crate(root.join("pkg/sdk"))
        .add_core_crate(root.join("pkg/core"))
        .add_core_crate(root.join("pkg/llm"))
        .add_core_crate(root.join("pkg/refstr"))
        .add_core_crate(root.join("pkg/tui"))
        .add_core_crate(root.join("pkg/build"))
        .add_core_crate(root.join("providers/anthropic"))
        .with_template(BootstrapTemplate);

    for m in modules {
        bundle = bundle.add_module(m);
    }

    // Resolve toolchain.
    let toolchain = Toolchain::resolve_system()?;
    eprintln!("toolchain: {} / {}", toolchain.rustc_path.display(), toolchain.cargo_path.display());
    bundle = bundle.with_toolchain(toolchain);

    // Generate workspace.
    eprintln!("generating workspace...");
    bundle.generate()?;

    let hash = bundle.content_hash()?;
    eprintln!("content hash: {}", &hash[..12]);

    // Compile.
    eprintln!("compiling...");
    let result = bundle.compile()?;

    if !result.warnings.is_empty() {
        eprintln!("{} warning(s)", result.warnings.len());
    }

    if result.success {
        if let Some(ref bin) = result.executable_path {
            eprintln!("success: {}", bin.display());
        } else {
            eprintln!("success (no binary path returned)");
        }
    } else {
        eprintln!("compilation failed:");
        eprintln!("{}", result.format_errors());
        return Err(mage_build::error::Error::Bundle("bootstrap compilation failed".into()));
    }

    Ok(())
}

