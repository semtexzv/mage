use std::env;
use std::path::PathBuf;
use std::process::ExitCode;

use mage_build::bundle::{Bundle, Config, RenderContext, Template};
use mage_build::error::Result;
use mage_build::module::{Dependency, Module};
use mage_build::toolchain::Toolchain;

fn main() -> ExitCode {
    let args: Vec<String> = env::args().skip(1).collect();

    match args.first().map(|s| s.as_str()) {
        Some("bootstrap") => run(bootstrap),
        Some("brew") => run(brew),
        Some("help") | None => {
            print_help();
            ExitCode::SUCCESS
        }
        Some(cmd) => {
            eprintln!("error: unknown command '{cmd}'");
            eprintln!();
            print_help();
            ExitCode::FAILURE
        }
    }
}

fn run(f: fn() -> Result<()>) -> ExitCode {
    match f() {
        Ok(()) => ExitCode::SUCCESS,
        Err(e) => {
            eprintln!("error: {e}");
            ExitCode::FAILURE
        }
    }
}

fn print_help() {
    eprintln!("usage: cargo xtask <command>");
    eprintln!();
    eprintln!("commands:");
    eprintln!("  bootstrap   Build generation-zero mage binary from workspace sources");
    eprintln!("  brew        Generate Homebrew formula for mage");
    eprintln!("  help        Show this help");
}

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------

/// Workspace root — xtask binary lives in `<root>/xtask/`, so go up one.
fn workspace_root() -> PathBuf {
    let manifest_dir = env::var("CARGO_MANIFEST_DIR")
        .map(PathBuf::from)
        .unwrap_or_else(|_| env::current_dir().unwrap());

    // If running via `cargo xtask`, CARGO_MANIFEST_DIR points to xtask/.
    // Walk up until we find the workspace Cargo.toml.
    let mut dir = manifest_dir.as_path();
    loop {
        let candidate = dir.join("Cargo.toml");
        if candidate.exists() {
            let content = std::fs::read_to_string(&candidate).unwrap_or_default();
            if content.contains("[workspace]") {
                return dir.to_path_buf();
            }
        }
        dir = match dir.parent() {
            Some(p) => p,
            None => {
                // Fallback — assume cwd.
                return env::current_dir().unwrap();
            }
        };
    }
}

