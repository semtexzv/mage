/// Find the mage workspace root by walking up from the current directory,
/// or from `MAGE_WORKSPACE_ROOT` env var.
pub fn find_workspace_root() -> Option<PathBuf> {
    if let Ok(root) = std::env::var("MAGE_WORKSPACE_ROOT") {
        let p = PathBuf::from(root);
        if p.join("Cargo.toml").exists() {
            return Some(p);
        }
    }

    let mut dir = std::env::current_dir().ok()?;
    loop {
        let cargo = dir.join("Cargo.toml");
        if cargo.exists() {
            if let Ok(content) = std::fs::read_to_string(&cargo) {
                if content.contains("[workspace]") && content.contains("pkg/core") {
                    return Some(dir);
                }
            }
        }
        if !dir.pop() {
            return None;
        }
    }
}
