//! Mage-specific build logic — template, workspace discovery, bundle assembly.
//!
//! This module provides:
//! - [`MageTemplate`] — the standard entry point template
//! - [`MageBuild`] — assembles a bundle from a workspace root + extension modroots
//!
//! All three compilation paths (xtask bootstrap, `mage bootstrap` subcommand,
//! agent-driven Recompile tool) use [`MageBuild`] to avoid duplication.

use std::path::PathBuf;

use crate::bundle::{Bundle, Config, RenderContext, Template};
use crate::compile::CompilationResult;
use crate::error::{Error, Result};
use crate::module::{self, Dependency};
use crate::toolchain::Toolchain;

// ---------------------------------------------------------------------------
// Template
// ---------------------------------------------------------------------------

/// The standard mage binary template.
///
/// Generates a `main.rs` that:
/// 1. Enters monitor mode if not already under one
/// 2. Collects built-in tools from `mage::tools::all()`
/// 3. Appends any discovered extension modules
/// 4. Calls `mage::app::run::run(modules)`
pub struct MageTemplate;

impl Template for MageTemplate {
    fn render_main(&self, ctx: &RenderContext) -> Result<String> {
        let mut mod_decls = String::new();
        let mut mod_calls = String::new();
        for m in ctx.modules {
            let mod_name = m.name.replace('-', "_");
            if m.init.is_some() {
                mod_decls.push_str(&format!(
                    "#[path = \"modules/{}.rs\"]\nmod {mod_name};\n",
                    m.name
                ));
                mod_calls.push_str(&format!(
                    "    modules.extend({mod_name}::modules());\n"
                ));
            }
        }

        Ok(format!(
            r##"//! Generated by mage-build — do not edit.

use std::rc::Rc;
use mage::prelude::*;

/// Embedded snapshot — all sources needed to rebuild this binary.
#[allow(dead_code)]
const SNAPSHOT: &[u8] = include_bytes!("snapshot.tar.zst");

{mod_decls}
fn main() {{
    // Register embedded snapshot for `mage snapshot` subcommand.
    mage::app::snapshot_cmd::set_snapshot(SNAPSHOT);

    // Monitor wrapping: if not running under a monitor, become one.
    if !mage::upgrade::is_agent_mode() {{
        std::process::exit(match mage::app::monitor::run_monitor() {{
            std::process::ExitCode::SUCCESS => 0,
            _ => 1,
        }});
    }}

    mage::run_local(|| async {{
        let mut modules: Vec<Rc<dyn Module>> = mage::tools::all();
{mod_calls}
        mage::app::run::run(modules).await;
    }});
}}
"##
        ))
    }

    fn render_dependencies(&self, _ctx: &RenderContext) -> Result<Vec<Dependency>> {
        Ok(vec![])
    }
}

// ---------------------------------------------------------------------------
// MageBuild — unified compilation pipeline
// ---------------------------------------------------------------------------

/// The core crate paths relative to a mage workspace root.
const CORE_CRATES: &[&str] = &[
    "pkg/sdk",
    "pkg/core",
    "pkg/tools",
    "pkg/llm",
    "pkg/refstr",
    "pkg/tui",
    "pkg/build",
    "pkg/app",
    "providers/anthropic",
];

/// Unified mage compilation.
///
/// All three paths (xtask bootstrap, `mage bootstrap`, Recompile tool)
/// use this to assemble and compile a binary.
pub struct MageBuild {
    /// Workspace root containing the mage source crates.
    workspace_root: PathBuf,
    /// Extension module directories to scan.
    extension_dirs: Vec<PathBuf>,
    /// Bundle name/id.
    name: String,
    /// Override the approot (default: `~/.mage`).
    config: Option<Config>,
}

impl MageBuild {
    /// Create from a workspace root.
    pub fn new(workspace_root: impl Into<PathBuf>) -> Self {
        Self {
            workspace_root: workspace_root.into(),
            extension_dirs: Vec::new(),
            name: "mage".into(),
            config: None,
        }
    }

    /// Set the bundle name (default: `"mage"`).
    pub fn name(mut self, name: impl Into<String>) -> Self {
        self.name = name.into();
        self
    }

    /// Override the build config (approot, conflict strategy).
    pub fn config(mut self, config: Config) -> Self {
        self.config = Some(config);
        self
    }

    /// Add a directory to scan for extension modules.
    pub fn extension_dir(mut self, dir: impl Into<PathBuf>) -> Self {
        self.extension_dirs.push(dir.into());
        self
    }

    /// Add the standard modroots: `~/.mage/modules/` and `.mage/modules/`.
    pub fn standard_extension_dirs(mut self) -> Self {
        if let Some(home) = dirs::home_dir() {
            self.extension_dirs.push(home.join(".mage/modules"));
        }
        if let Ok(cwd) = std::env::current_dir() {
            self.extension_dirs.push(cwd.join(".mage/modules"));
        }
        self
    }

    /// Compile and return the result.
    pub fn compile(self) -> Result<CompilationResult> {
        let root = &self.workspace_root;

        // Verify workspace exists.
        if !root.join("Cargo.toml").exists() {
            return Err(Error::Bundle(format!(
                "workspace root not found: {}",
                root.display()
            )));
        }

        // Scan extension directories.
        let mut modules = Vec::new();
        for dir in &self.extension_dirs {
            let found = module::scan_directory(dir);
            for m in &found {
                eprintln!("  found module: {} ({})", m.name, m.path.display());
            }
            modules.extend(found);
        }
        if modules.is_empty() {
            eprintln!("  no extension modules found (built-in tools are always included)");
        }

        // Assemble bundle.
        let config = self.config.unwrap_or_default();
        let mut bundle = Bundle::new(&self.name).with_config(config);

        for rel in CORE_CRATES {
            bundle = bundle.add_core_crate(root.join(rel));
        }
        bundle = bundle.with_template(MageTemplate);

        for m in modules {
            bundle = bundle.add_module(m);
        }

        let toolchain = Toolchain::resolve_system()?;
        eprintln!(
            "toolchain: {} / {}",
            toolchain.rustc_path.display(),
            toolchain.cargo_path.display()
        );
        bundle = bundle.with_toolchain(toolchain);

        eprintln!("generating workspace...");
        bundle.generate()?;

        let hash = bundle.content_hash()?;
        eprintln!("content hash: {}", &hash[..12]);

        eprintln!("compiling...");
        bundle.compile()
    }
}

