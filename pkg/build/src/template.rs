//! Mage-specific build logic — template, workspace discovery, bundle assembly.
//!
//! This module provides:
//! - [`MageTemplate`] — the standard entry point template
//! - [`MageBuild`] — assembles a bundle from a workspace root + extension modroots
//!
//! All three compilation paths (xtask bootstrap, `mage bootstrap` subcommand,
//! agent-driven Recompile tool) use [`MageBuild`] to avoid duplication.

use std::path::PathBuf;

use crate::bundle::{Bundle, Config, RenderContext, Template};
use crate::compile::CompilationResult;
use crate::error::{Error, Result};
use crate::module::{self, Dependency};
use crate::toolchain::Toolchain;

// ---------------------------------------------------------------------------
// Template
// ---------------------------------------------------------------------------

/// The standard mage binary template.
///
/// Generates a `main.rs` that:
/// 1. Enters monitor mode if not already under one
/// 2. Collects built-in tools from `mage::tools::all()`
/// 3. Appends any discovered extension modules
/// 4. Calls `mage::app::run::run(modules)`
pub struct MageTemplate;

impl Template for MageTemplate {
    fn render_main(&self, ctx: &RenderContext) -> Result<String> {
        let mut mod_decls = String::new();
        let mut mod_calls = String::new();
        for m in ctx.modules {
            let mod_name = m.name.replace('-', "_");
            if m.init.is_some() {
                mod_decls.push_str(&format!(
                    "#[path = \"modules/{}.rs\"]\nmod {mod_name};\n",
                    m.name
                ));
                mod_calls.push_str(&format!(
                    "    modules.extend({mod_name}::modules());\n"
                ));
            }
        }

        Ok(format!(
            r##"//! Generated by mage-build — do not edit.

use std::rc::Rc;
use mage::prelude::*;

/// Embedded snapshot — all sources needed to rebuild this binary.
#[allow(dead_code)]
const SNAPSHOT: &[u8] = include_bytes!("snapshot.tar.zst");

{mod_decls}
fn main() {{
    // Register embedded snapshot.
    mage::upgrade::set_snapshot(SNAPSHOT);

    // Handle non-interactive subcommands before monitor wrapping.
    // These don't need a TUI or agent loop.
    if let Some(cmd) = std::env::args().nth(1) {{
        match cmd.as_str() {{
            "snapshot" => {{
                let args: Vec<String> = std::env::args().skip(2).collect();
                mage::app::snapshot_cmd::run_snapshot(&args);
                return;
            }}
            "rebuild" => {{
                mage::app::rebuild::run_rebuild();
                return;
            }}
            _ => {{}}
        }}
    }}

    // Monitor wrapping: if not running under a monitor, become one.
    if !mage::upgrade::is_agent_mode() {{
        std::process::exit(match mage::app::monitor::run_monitor() {{
            std::process::ExitCode::SUCCESS => 0,
            _ => 1,
        }});
    }}

    mage::run_local(|| async {{
        let mut modules: Vec<Rc<dyn Module>> = mage::tools::all();
{mod_calls}
        mage::app::run::run(modules).await;
    }});
}}
"##
        ))
    }

    fn render_dependencies(&self, _ctx: &RenderContext) -> Result<Vec<Dependency>> {
        Ok(vec![])
    }
}

